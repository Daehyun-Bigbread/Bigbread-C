#include<stdio.h>

void main()
{
	int i,j,k,a[4][4];
	//입력 
	for(i=0;i<=2;i++)
	{
		for(j=0;j<=2;j++)
		{
			k=k+1;
			a[i][j]=k;
		}
	}
	//처리
	a[0][3]=a[0][0]+a[0][1]+a[0][2];//0행의합 
	a[1][3]=a[1][0]+a[1][1]+a[1][2];//1행의합 
	a[2][3]=a[2][0]+a[2][1]+a[2][2];//2행의합
	
	a[3][0]=a[0][0]+a[1][0]+a[2][0];//0열의합 
	a[3][1]=a[0][1]+a[1][1]+a[2][1];//1열의합 
	a[3][2]=a[0][2]+a[1][2]+a[2][2];//2열의합 
	
	a[3][3]=a[3][0]+a[3][1]+a[3][2];//3행의합 
	a[3][3]=a[0][3]+a[1][3]+a[2][3];//3열의합 
	
	
	//출력
	for(i=0;i<=3;i++)
	{
		for(j=0;j<=3;j++)
		{
			printf("%4d",a[i][j]);
		}
			printf("\n");
	}//열의 종료
}//행의 종료 
